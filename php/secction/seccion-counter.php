    // Definir la fecha esperada (puedes cambiarla a la fecha que desees)
    var fechaEsperada = new Date('2024-07-27');

    // Función para actualizar el contador
    function actualizarContador() {
        var ahora = new Date();
        var diferencia = fechaEsperada - ahora;

        var dias = Math.floor(diferencia / (1000 * 60 * 60 * 24));
        var meses = Math.floor(diferencia / (1000 * 60 * 60 * 24 * 30.44));
        var horas = Math.floor((diferencia % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
        var minutos = Math.floor((diferencia % (1000 * 60 * 60)) / (1000 * 60));
        var segundos = Math.floor((diferencia % (1000 * 60)) / 1000);

        // Actualiza el HTML con los nuevos valores
        document.getElementById('dias').innerText = dias;
        document.getElementById('meses').innerText = meses;
        document.getElementById('horas').innerText = horas;
        document.getElementById('minutos').innerText = minutos;
        document.getElementById('segundos').innerText = segundos;
    }

    // Llama a la función inicialmente para que comience a contar
    actualizarContador();

    // Actualiza el contador cada segundo
    setInterval(actualizarContador, 1000);
</script>